#include "createjsoncharactor.h"
#include "createjsonnomove.h"

//===================================================================
// 生成処理
//===================================================================
void CJsonCreateCharactor::Create(const json& Jsondata)
{
	// タグの一致を見る
	std::string type = Jsondata["Charactortag"];

	// 動かないキャラクターの生成
	if (type == "NoMove") CJsonCreateNoMove::Create(Jsondata);
}