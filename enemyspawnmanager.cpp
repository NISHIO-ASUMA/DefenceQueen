void CEnemySpawnManager::Uninit(void)
{
	// 配列の破棄
	for (auto Spawn : m_pSpawners)
	{
		if (Spawn)
		{
			Spawn->Uninit();
			delete Spawn;
			Spawn = nullptr;
		}
	}
}
